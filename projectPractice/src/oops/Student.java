package oops;

public class Student {
private String name;
private int Id;
private char Section;
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getId() {
	return Id;
}
public void setId(int id) {
	Id = id;
}
public char getSection() {
	return Section;
}
public void setSection(char section) {
	Section = section;
}

}
